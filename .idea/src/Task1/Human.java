package Task1;

public class Human {
    private int weight;
    private int height;
    private String carBrand;

    public Human(int weight, int height, String carBrand) {
        this.weight = weight;
        this.height = height;
        this.carBrand = carBrand;
    }

    public int getWeight() {
        return weight;
    }

    public int getHeight() {
        return height;
    }

    public String getCarBrand() {
        return carBrand;
    }
}
